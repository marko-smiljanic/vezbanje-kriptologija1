    private void verifikujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verifikujActionPerformed
        try {
            String hexHmacVerifikacija = this.obj.hexHmac(bobEditor.getText().getBytes(), algoritamBob.getSelectedItem().toString(), kljuc);
            if(hmacAlisa2.equals(hexHmacVerifikacija)){
                this.hmacBob.setText(hexHmacVerifikacija);
                this.hmacBob.setBackground(Color.GREEN);
            }else{
                this.hmacBob.setText(hexHmacVerifikacija);
                this.hmacBob.setBackground(Color.RED);   
            }
        
        
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(hmacGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(hmacGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_verifikujActionPerformed

