    private void posaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_posaljiActionPerformed
        try {
            this.kljuc = this.obj.generisiKljucHmac(this.algoritamAlisa.getSelectedItem().toString());
            String hmac = this.obj.hexHmac(alisaEditor.getText().getBytes(), this.algoritamAlisa.getSelectedItem().toString(), kljuc);
            this.hmacAlisa.setText(hmac);
            this.hmacAlisa2.setText(hmac);
            this.bobEditor.setText(this.alisaEditor.getText());
            this.hmacAlisa2.setBackground(Color.GREEN); 
            
              
            
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(hmacGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(hmacGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_posaljiActionPerformed

